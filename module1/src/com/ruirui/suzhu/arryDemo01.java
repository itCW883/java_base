package com.ruirui.suzhu;

public class arryDemo01 {
    public static void main(String[] args) {
        /*获取数组长度:数组名.length;切记不要加()
        *       带()都是方法
        * length是数组的属性不是方法*/
        int arry[]=new int[]{1,1,1,1};
        int a=arry.length;
        System.out.println("a = " + a);

        /*索引:元素在数组中存储的位置(下标)
        *       索引从0开始,索引是唯一的不能重复
        *       最大索引为:数组长度-1
        * 作用:操作元素通过索引来进行(存,取,查均要通过索引)*/
    }
}
